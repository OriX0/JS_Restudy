/* 规则 匹配 24小时制的时间
时针：
  1. 0 或 1 后面跟随一个数字
  2. 2 后面跟随 0-3
分针
  0-5后面跟着一个随机数字
*/
let reg = /([01]\d|2[0-3]):[0-5]\d/g;
alert("00:00 10:10 23:59 25:99 1:2".match(reg)); // 00:00,10:10,23:59